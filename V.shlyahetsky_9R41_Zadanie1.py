# Ввод данных от пользователя
temperature = int(input("Введите температуру: "))

# Если температура ниже 0, не спрашиваем про осадки
if temperature <= 0:
    # Рекомендации для морозной погоды
    print("Надень пуховик.")
else:
    # Для температуры 0 и выше спрашиваем про осадки
    is_rainy = input("Идет дождь? (да или нет): ").lower() == 'да'
    is_raining_heavily = False
    
    # Если идет дождь, уточняем его интенсивность
    if is_rainy:
        is_raining_heavily = input("Дождь сильный? (да или нет): ").lower() == 'да'
    
    # Логика выбора одежды по температуре (для температуры 0 и выше)
    if temperature <= 10:
        # Если температура от 0 до 10 градусов
        print("Надень теплую куртку.")
    else:
        if temperature <= 15:
            # Если температура от 10 до 15 градусов
            print("Надень легкую куртку.")
        else:
            if temperature <= 20:
                # Если температура от 15 до 20 градусов
                print("Надень свитер или кофту.")
            else:
                # Если температура выше 20 градусов
                print("Можно надеть футболку.")
    
    # Дополнительные рекомендации для дождливой погоды
    if is_raining_heavily:
        # Если идет сильный дождь
        print("Возьми зонт и надень дождевик.")
    else:
        if is_rainy:
            # Если идет обычный дождь
            print("Возьми зонт.")